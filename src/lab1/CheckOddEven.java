package lab1;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = 49;
        System.out.println("The is number" + number);
        if (number % 2 == 0) {
            System.out.println("even number");
        } else {
            System.out.println("odd number");
        }
        System.out.println("Bye!");
    }

}
